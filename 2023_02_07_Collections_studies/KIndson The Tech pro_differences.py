#DIFFERENCE BETWEEN 
# LIST - square braces [ ]
# TUPLE - rounded braces ( )
# SET - the set keywrod ([ ])
# DICTIONARY - curly braces { } made up of key-value pairs.

list1 = ["Computer", "Printer", "TV", "Camera", 89,30.8]
print(list1)

tuple1 = ("Computer", "Printer", "TV", "Camera", 89, 30.8)
print(tuple1)

set1 = set(["Computer", "Printer", "TV", "Camera", 89, 30.8])
print(set1)

dict1 = {
    1: "Monday",
    2: "Tuesday",
    3: "Wednesday"
}
print(dict1)




